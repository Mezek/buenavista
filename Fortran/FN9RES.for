      IMPLICIT DOUBLE PRECISION (A-H,O-Z)
      COMMON/UNIT/ISYSRD,ISYSWR
      COMPLEX*16 CI,CE,CH,C1S,C2S,C1Q,C2Q
      COMPLEX*16 CK1S,CZ1S,C1V,CK2S,CZ2S,C2V
      COMPLEX*16 CK1Q,CZ1Q,C1W,CK2Q,CZ2Q,C2W
      COMPLEX*16 CEOM,CSOM,CD1K1,CD1Z1,C1V1,C1V1C
      COMPLEX*16 CD2K1,CD2Z1,C2V1,C2V1C
      COMPLEX*16 CEFI,CSFI,CD1K2,CD1Z2,C1V2,C1V2C
      COMPLEX*16 CD2K2,CD2Z2,C2V2,C2V2C
      COMPLEX*16 CEOM1,CSOM1,CD1K3,CD1Z3,C1V3,C1V3C
      COMPLEX*16 CD2K3,CD2Z3,C2V3,C2V3C
      COMPLEX*16 CEF1,CSF1,CD1K4,CD1Z4,C1V4,C1V4C
      COMPLEX*16 CD2K4,CD2Z4,C2V4,C2V4C
      COMPLEX*16 CEOM2,CSOM2,CD1K5,CD1Z5,C1V5,C1V5C
      COMPLEX*16 CD2K5,CD2Z5,C2V5,C2V5C
      COMPLEX*16 CEF2,CSF2,CD1K6,CD1Z6,C1V6,C1V6C
      COMPLEX*16 CD2K6,CD2Z6,C2V6,C2V6C
      COMPLEX*16 CHRO,CQRO,CB1K1,CB1Z1,C1W1,C1W1C
      COMPLEX*16 CB2K1,CB2Z1,C2W1,C2W1C
      COMPLEX*16 CHR1,CQR1,CB1K2,CB1Z2,C1W2,C1W2C
      COMPLEX*16 CB2K2,CB2Z2,C2W2,C2W2C
      COMPLEX*16 CHR2,CQR2,CB1K3,CB1Z3,C1W3,C1W3C
      COMPLEX*16 CB2K3,CB2Z3,C2W3,C2W3C
      COMPLEX*16 COM1V,CFI1V,CO11V,CF11V,CO21V,CF21V
      COMPLEX*16 COM1VN,CFI1VN,CO11VN,CF11VN,CO21VN,CF21VN
      COMPLEX*16 CF21VD
      COMPLEX*16 COM2V,CFI2V,CO12V,CF12V,CO22V,CF22V
      COMPLEX*16 COM2VN,CFI2VN,CO12VN,CF12VN,CO22VN,CF22VN
      COMPLEX*16 CF22VD
      COMPLEX*16 CRO1W,CR11W,CR21W
      COMPLEX*16 CRO1WN,CR11WN,CR21WN
      COMPLEX*16 CRO2W,CR12W,CR22W
      COMPLEX*16 CRO2WN,CR12WN,CR22WN
      COMPLEX*16 DOM1V,DFI1V,DO11V,DF11V,DO21V,DF21V
      COMPLEX*16 DOM2V,DFI2V,DO12V,DF12V,DO22V,DF22V
      COMPLEX*16 DRO1W,DR11W,DR21W
      COMPLEX*16 CFN1S,CFN2S,CFN1V,CFN2V,CGEP,CGMP,CGEN,CGMN
      COMPLEX*16 CU(556)
      COMPLEX*16 CD2S1,CD2S2,CD2S3
      DIMENSION A(12),G(12),T(556),R(556),DR(556)
      DIMENSION AH(556),FCHI(556)

      GO TO (10,20,30,20)IFLAG
10    CONTINUE
      READ (5,11) (T(I),R(I),DR(I),I=1,556)
11    FORMAT(F12.4,2F12.5)
      CI=(0.D0,1.D0)
      PI=3.1415926D0
      HOM=5.590357D0
      WOM=0.0642857D0
      HFI=7.281821D0
      WFI=0.0304286D0
      HOM1=10.178571D0
      WOM1=1.535714D0
      HF1=12.D0
      WF1=1.07143D0
      HOM2=11.92857D0
      WOM2=2.25D0
      HF2=15.535714D0
      WF2=0.435714D0
      HRO=5.539214D0
      WRO=1.065000D0
      HRO1=10.464285D0
      WRO1=2.857143D0
      HRO2=12.285714D0
      WRO2=1.785714D0

      AMMP=1.792847356D0
      AMMN=-1.9130427D0
      ALF=1.D0/137.036D0
      ALF2=ALF**2
      HP=6.7019429D0
      HPG=HP*0.14D0
      HN=6.7111810D0
      HNG=HN*0.14D0

      RETURN
20    CONTINUE
      F=0.0D0
      J=0
      DO 41 I=1,556
      S=T(I)/.0196D0
      CE=S-9.D0
      CH=S-4.D0
      C1S=CDSQRT(CE/9.D0)
      C2S=CDSQRT(CE/9.D0)
      C1Q=CDSQRT(CH/4.D0)
      C2Q=CDSQRT(CH/4.D0)
      V1N=A(1)-DSQRT(A(1)**2+1.D0)
      V2N=A(2)-DSQRT(A(2)**2+1.D0)
      IF(DREAL(C1S).LE.A(1)) GO TO 4
      C1S=C1S+CI*0.000001D0
4     CONTINUE
      IF(DREAL(C2S).LE.A(2)) GO TO 14
      C2S=C2S+CI*0.000001D0
14    CONTINUE
      W1N=A(3)-DSQRT(A(3)**2+1.D0)
      W2N=A(4)-DSQRT(A(4)**2+1.D0)
      IF(DREAL(C1Q).LE.A(3)) GO TO 5
      C1Q=C1Q+CI*0.000001D0
5     CONTINUE
      IF(DREAL(C2Q).LE.A(4)) GO TO 15
      C2Q=C2Q+CI*0.000001D0
15    CONTINUE

      CK1S=CDSQRT(A(1)+C1S)
      CZ1S=CDSQRT(A(1)-C1S)
      C1V=CI*(CK1S-CZ1S)/(CK1S+CZ1S)

      CK2S=CDSQRT(A(2)+C2S)
      CZ2S=CDSQRT(A(2)-C2S)
      C2V=CI*(CK2S-CZ2S)/(CK2S+CZ2S)

      CK1Q=CDSQRT(A(3)+C1Q)
      CZ1Q=CDSQRT(A(3)-C1Q)
      C1W=CI*(CK1Q-CZ1Q)/(CK1Q+CZ1Q)

      CK2Q=CDSQRT(A(4)+C2Q)
      CZ2Q=CDSQRT(A(4)-C2Q)
      C2W=CI*(CK2Q-CZ2Q)/(CK2Q+CZ2Q)

      CEOM=(HOM-CI*WOM/2.D0)**2-9.D0
      CSOM=CDSQRT(CEOM/9.D0)

      CD1K1=CDSQRT(A(1)+CSOM)
      CD1Z1=CDSQRT(A(1)-CSOM)
      C1V1=CI*(CD1K1-CD1Z1)/(CD1K1+CD1Z1)

      CD2K1=CDSQRT(A(2)+CSOM)
      CD2Z1=CDSQRT(A(2)-CSOM)
      C2V1=CI*(CD2K1-CD2Z1)/(CD2K1+CD2Z1)

      CEFI=(HFI-CI*WFI/2.D0)**2-9.D0
      CSFI=CDSQRT(CEFI/9.D0)

      CD1K2=CDSQRT(A(1)+CSFI)
      CD1Z2=CDSQRT(A(1)-CSFI)
      C1V2=CI*(CD1K2-CD1Z2)/(CD1K2+CD1Z2)

      CD2K2=CDSQRT(A(2)+CSFI)
      CD2Z2=CDSQRT(A(2)-CSFI)
      C2V2=CI*(CD2K2-CD2Z2)/(CD2K2+CD2Z2)

      CEOM1=(HOM1-CI*WOM1/2.D0)**2-9.D0
      CSOM1=CDSQRT(CEOM1/9.D0)

      CD1K3=CDSQRT(A(1)+CSOM1)
      CD1Z3=CDSQRT(A(1)-CSOM1)
      C1V3=CI*(CD1K3+CD1Z3)/(CD1K3-CD1Z3)

      CD2K3=CDSQRT(A(2)+CSOM1)
      CD2Z3=CDSQRT(A(2)-CSOM1)
      C2V3=CI*(CD2K3+CD2Z3)/(CD2K3-CD2Z3)

      CEF1=(HF1-CI*WF1/2.D0)**2-9.D0
      CSF1=CDSQRT(CEF1/9.D0)

      CD1K4=CDSQRT(A(1)+CSF1)
      CD1Z4=CDSQRT(A(1)-CSF1)
      C1V4=CI*(CD1K4+CD1Z4)/(CD1K4-CD1Z4)

      CD2K4=CDSQRT(A(2)+CSF1)
      CD2Z4=CDSQRT(A(2)-CSF1)
      C2V4=CI*(CD2K4+CD2Z4)/(CD2K4-CD2Z4)

      CEOM2=(HOM2-CI*WOM2/2.D0)**2-9.D0
      CSOM2=CDSQRT(CEOM2/9.D0)

      CD1K5=CDSQRT(A(1)+CSOM2)
      CD1Z5=CDSQRT(A(1)-CSOM2)
      C1V5=CI*(CD1K5+CD1Z5)/(CD1K5-CD1Z5)

      CD2K5=CDSQRT(A(2)+CSOM2)
      CD2Z5=CDSQRT(A(2)-CSOM2)
      C2V5=CI*(CD2K5+CD2Z5)/(CD2K5-CD2Z5)

      CEF2=(HF2-CI*WF2/2.D0)**2-9.D0
      CSF2=CDSQRT(CEF2/9.D0)

      CD1K6=CDSQRT(A(1)+CSF2)
      CD1Z6=CDSQRT(A(1)-CSF2)
      C1V6=CI*(CD1K6+CD1Z6)/(CD1K6-CD1Z6)

      CD2K6=CDSQRT(A(2)+CSF2)
      CD2Z6=CDSQRT(A(2)-CSF2)
      C2V6=CI*(CD2K6+CD2Z6)/(CD2K6-CD2Z6)

      CHRO=(HRO-CI*WRO/2.D0)**2-4.D0
      CQRO=CDSQRT(CHRO/4.D0)

      CB1K1=CDSQRT(A(3)+CQRO)
      CB1Z1=CDSQRT(A(3)-CQRO)
      C1W1=CI*(CB1K1-CB1Z1)/(CB1K1+CB1Z1)

      CB2K1=CDSQRT(A(4)+CQRO)
      CB2Z1=CDSQRT(A(4)-CQRO)
      C2W1=CI*(CB2K1-CB2Z1)/(CB2K1+CB2Z1)

      CHR1=(HRO1-CI*WRO1/2.D0)**2-4.D0
      CQR1=CDSQRT(CHR1/4.D0)

      CB1K2=CDSQRT(A(3)+CQR1)
      CB1Z2=CDSQRT(A(3)-CQR1)
      C1W2=CI*(CB1K2-CB1Z2)/(CB1K2+CB1Z2)

      CB2K2=CDSQRT(A(4)+CQR1)
      CB2Z2=CDSQRT(A(4)-CQR1)
      C2W2=CI*(CB2K2-CB2Z2)/(CB2K2+CB2Z2)

      CHR2=(HRO2-CI*WRO2/2.D0)**2-4.D0
      CQR2=CDSQRT(CHR2/4.D0)

      CB1K3=CDSQRT(A(3)+CQR2)
      CB1Z3=CDSQRT(A(3)-CQR2)
      C1W3=CI*(CB1K3+CB1Z3)/(CB1K3-CB1Z3)

      CB2K3=CDSQRT(A(4)+CQR2)
      CB2Z3=CDSQRT(A(4)-CQR2)
      C2W3=CI*(CB2K3+CB2Z3)/(CB2K3-CB2Z3)

      SAMM=0.5D0*(AMMP+AMMN)
      DAMM=0.5D0*(AMMP-AMMN)

      C1V1C=DCONJG(C1V1)
      C1V2C=DCONJG(C1V2)
      C1V3C=DCONJG(C1V3)
      C1V4C=DCONJG(C1V4)
      C1V5C=DCONJG(C1V5)
      C1V6C=DCONJG(C1V6)

      C2V1C=DCONJG(C2V1)
      C2V2C=DCONJG(C2V2)
      C2V3C=DCONJG(C2V3)
      C2V4C=DCONJG(C2V4)
      C2V5C=DCONJG(C2V5)
      C2V6C=DCONJG(C2V6)

      C1W1C=DCONJG(C1W1)
      C1W2C=DCONJG(C1W2)
      C1W3C=DCONJG(C1W3)

      C2W1C=DCONJG(C2W1)
      C2W2C=DCONJG(C2W2)
      C2W3C=DCONJG(C2W3)

      COM1VN=((V1N-C1V1)*(V1N-C1V1C)*(V1N-1.D0/C1V1)*(V1N-1.D0/C1V1C))
      COM1V=COM1VN/
     /((C1V-C1V1)*(C1V-C1V1C)*(C1V-1.D0/C1V1)*(C1V-1.D0/C1V1C))

      CFI1VN=((V1N-C1V2)*(V1N-C1V2C)*(V1N-1.D0/C1V2)*(V1N-1.D0/C1V2C))
      CFI1V=CFI1VN/
     /((C1V-C1V2)*(C1V-C1V2C)*(C1V-1.D0/C1V2)*(C1V-1.D0/C1V2C))

      CO11VN=((V1N-C1V3)*(V1N-C1V3C)*(V1N+C1V3)*(V1N+C1V3C))
      CO11V=CO11VN/
     /((C1V-C1V3)*(C1V-C1V3C)*(C1V+C1V3)*(C1V+C1V3C))

      CF11VN=((V1N-C1V4)*(V1N-C1V4C)*(V1N+C1V4)*(V1N+C1V4C))
      CF11V=CF11VN/
     /((C1V-C1V4)*(C1V-C1V4C)*(C1V+C1V4)*(C1V+C1V4C))

      CO21VN=((V1N-C1V5)*(V1N-C1V5C)*(V1N+C1V5)*(V1N+C1V5C))
      CO21V=CO21VN/
     /((C1V-C1V5)*(C1V-C1V5C)*(C1V+C1V5)*(C1V+C1V5C))

      CF21VN=((V1N-C1V6)*(V1N-C1V6C)*(V1N+C1V6)*(V1N+C1V6C))
      CF21VD=((C1V-C1V6)*(C1V-C1V6C)*(C1V+C1V6)*(C1V+C1V6C))

      CF21V=CF21VN/CF21VD

      DOM1V=COM1VN/DREAL(-(C1V1-1.D0/C1V1)*(C1V1C-1.D0/C1V1C))
      DFI1V=CFI1VN/DREAL(-(C1V2-1.D0/C1V2)*(C1V2C-1.D0/C1V2C))
      DO11V=CO11VN/DREAL(-(C1V3-1.D0/C1V3)*(C1V3C-1.D0/C1V3C))
      DF11V=CF11VN/DREAL(-(C1V4-1.D0/C1V4)*(C1V4C-1.D0/C1V4C))
      DO21V=CO21VN/DREAL(-(C1V5-1.D0/C1V5)*(C1V5C-1.D0/C1V5C))
      DF21V=CF21VN/DREAL(-(C1V6-1.D0/C1V6)*(C1V6C-1.D0/C1V6C))

      CFN1S=((1.D0-C1V**2)/(1.D0-V1N**2))**4*
     *(0.5D0*CO21V*CF21V+
     +(CF21V*CO11V*(DF21V-DO11V)/
     /(DF21V-DO21V)+CO21V*CO11V*(DO21V-DO11V)/
     /(DO21V-DF21V)-CO21V*CF21V)*A(7)+
     +(CF11V*CF21V*(DF21V-DF11V)/
     /(DF21V-DO21V)+COM2V*CF11V*(DO21V-DF11V)/
     /(DO21V-DF21V)-CO21V*CF21V)*A(8)+
     +(CF21V*COM1V*(DF21V-DOM1V)/
     /(DF21V-DO21V)+COM1V*CO21V*(DO21V-DOM1V)/
     /(DO21V-DF21V)-CO21V*CF21V)*A(5)+
     +(CFI1V*CF21V*(DF21V-DFI1V)/(DF21V-DO21V)+
     +CO21V*CFI1V*(DO21V-DFI1V)/
     /(DO21V-DF21V)-CO21V*CF21V)*A(6))

      COM2VN=((V2N-C2V1)*(V2N-C2V1C)*(V2N-1.D0/C2V1)*(V2N-1.D0/C2V1C))
      COM2V=COM2VN/
     /((C2V-C2V1)*(C2V-C2V1C)*(C2V-1.D0/C2V1)*(C2V-1.D0/C2V1C))

      CFI2VN=((V2N-C2V2)*(V2N-C2V2C)*(V2N-1.D0/C2V2)*(V2N-1.D0/C2V2C))
      CFI2V=CFI2VN/
     /((C2V-C2V2)*(C2V-C2V2C)*(C2V-1.D0/C2V2)*(C2V-1.D0/C2V2C))

      CO12VN=((V2N-C2V3)*(V2N-C2V3C)*(V2N+C2V3)*(V2N+C2V3C))
      CO12V=CO12VN/
     /((C2V-C2V3)*(C2V-C2V3C)*(C2V+C2V3)*(C2V+C2V3C))

      CF12VN=((V2N-C2V4)*(V2N-C2V4C)*(V2N+C2V4)*(V2N+C2V4C))
      CF12V=CF12VN/
     /((C2V-C2V4)*(C2V-C2V4C)*(C2V+C2V4)*(C2V+C2V4C))

      CO22VN=((V2N-C2V5)*(V2N-C2V5C)*(V2N+C2V5)*(V2N+C2V5C))
      CO22V=CO22VN/
     /((C2V-C2V5)*(C2V-C2V5C)*(C2V+C2V5)*(C2V+C2V5C))

      CF22VN=((V2N-C2V6)*(V2N-C2V6C)*(V2N+C2V6)*(V2N+C2V6C))
      CF22V=CF22VN/
     /((C2V-C2V6)*(C2V-C2V6C)*(C2V+C2V6)*(C2V+C2V6C))

      DOM2V=COM2VN/DREAL(-(C2V1-1.D0/C2V1)*(C2V1C-1.D0/C2V1C))
      DFI2V=CFI2VN/DREAL(-(C2V2-1.D0/C2V2)*(C2V2C-1.D0/C2V2C))
      DO12V=CO12VN/DREAL(-(C2V3-1.D0/C2V3)*(C2V3C-1.D0/C2V3C))
      DF12V=CF12VN/DREAL(-(C2V4-1.D0/C2V4)*(C2V4C-1.D0/C2V4C))
      DO22V=CO22VN/DREAL(-(C2V5-1.D0/C2V5)*(C2V5C-1.D0/C2V5C))
      DF22V=CF22VN/DREAL(-(C2V6-1.D0/C2V6)*(C2V6C-1.D0/C2V6C))

      CD2S1=(CF12V*CO12V*CF22V*((DF22V-DF12V)*(DO12V-DF12V))/
     /((DF22V-DO22V)*(DO12V-DO22V))+
     +CO22V*CO12V*CF12V*((DO22V-DF12V)*(DO12V-DF12V))/
     /((DO22V-DF22V)*(DO12V-DF22V))+
     +CO22V*CF22V*CF12V*((DO22V-DF12V)*(DF22V-DF12V))/
     /((DO22V-DO12V)*(DF22V-DO12V))-CO22V*CF22V*CO12V)*A(11)

      CD2S2=(CF22V*CO12V*COM2V*((DF22V-DOM2V)*(DO12V-DOM2V))/
     /((DF22V-DO22V)*(DO12V-DO22V))+
     +COM2V*CO12V*CO22V*((DO22V-DOM2V)*(DO12V-DOM2V))/
     /((DO22V-DF22V)*(DO12V-DF22V))+
     +COM2V*CF22V*CO22V*((DO22V-DOM2V)*(DF22V-DOM2V))/
     /((DO22V-DO12V)*(DF22V-DO12V))-CO22V*CF22V*CO12V)*A(9)

      CD2S3=(CFI2V*CO12V*CF22V*((DF22V-DFI2V)*(DO12V-DFI2V))/
     /((DF22V-DO22V)*(DO12V-DO22V))+
     +CFI2V*CO12V*CO22V*((DO22V-DFI2V)*(DO12V-DFI2V))/
     /((DO22V-DF22V)*(DO12V-DF22V))+
     +CO22V*CFI2V*CF22V*((DO22V-DFI2V)*(DF22V-DFI2V))/
     /((DO22V-DO12V)*(DF22V-DO12V))-CO22V*CF22V*CO12V)*A(10)


      CFN2S=((1.D0-C2V**2)/(1.D0-V2N**2))**6*
     *(SAMM*CO22V*CF22V*CO12V+CD2S1+CD2S2+CD2S3)

      CRO1WN=((W1N-C1W1)*(W1N-C1W1C)*(W1N-1.D0/C1W1)*(W1N-1.D0/C1W1C))
      CRO1W=CRO1WN/
     /((C1W-C1W1)*(C1W-C1W1C)*(C1W-1.D0/C1W1)*(C1W-1.D0/C1W1C))

      CR11WN=((W1N-C1W2)*(W1N-C1W2C)*(W1N-1.D0/C1W2)*(W1N-1.D0/C1W2C))
      CR11W=CR11WN/
     /((C1W-C1W2)*(C1W-C1W2C)*(C1W-1.D0/C1W2)*(C1W-1.D0/C1W2C))

      CR21WN=((W1N-C1W3)*(W1N-C1W3C)*(W1N+C1W3)*(W1N+C1W3C))
      CR21W=CR21WN/
     /((C1W-C1W3)*(C1W-C1W3C)*(C1W+C1W3)*(C1W+C1W3C))

      DRO1W=CRO1WN/DREAL(-(C1W1-1.D0/C1W1)*(C1W1C-1.D0/C1W1C))
      DR11W=CR11WN/DREAL(-(C1W2-1.D0/C1W2)*(C1W2C-1.D0/C1W2C))
      DR21W=CR21WN/DREAL(-(C1W3-1.D0/C1W3)*(C1W3C-1.D0/C1W3C))

      CFN1V=((1.D0-C1W**2)/(1.D0-W1N**2))**4*
     *(0.5D0*CR21W*CR11W+
     +(CRO1W*CR11W*(DR11W-DRO1W)/(DR11W-DR21W)+
     +CRO1W*CR21W*(DR21W-DRO1W)/(DR21W-DR11W)-
     -CR21W*CR11W)*A(12))

      CRO2WN=((W2N-C2W1)*(W2N-C2W1C)*(W2N-1.D0/C2W1)*(W2N-1.D0/C2W1C))
      CRO2W=CRO2WN/
     /((C2W-C2W1)*(C2W-C2W1C)*(C2W-1.D0/C2W1)*(C2W-1.D0/C2W1C))

      CR12WN=((W2N-C2W2)*(W2N-C2W2C)*(W2N-1.D0/C2W2)*(W2N-1.D0/C2W2C))
